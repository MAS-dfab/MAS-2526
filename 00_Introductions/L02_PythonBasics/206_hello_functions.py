#Now let's get on to functions.
#Functions are a way to organize your code into reusable blocks.
#Functions are defined using the def keyword.
#This is how you define a function.
def my_function():
    print("Hello from a function")
#This is how you call a function.
my_function()
#Functions are good for organizing your code and making it reusable.
#You can pass data, known as parameters, into a function.
#This is how you define a function with parameters.
def my_function_with_parameters(fname, lname):
    print(fname + " " + lname)
#This is how you call a function with parameters   
my_function_with_parameters("Johnny", "Marr")

#Now let's run this program.